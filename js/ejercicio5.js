let primerNumero = parseInt(prompt("Por favor ingrese el primer número:"));
let segundoNumero = parseInt(prompt("Por favor ingrese en segundo número"));
document.write("El resultado de la suma de los números ingresados es: " + (primerNumero + segundoNumero));